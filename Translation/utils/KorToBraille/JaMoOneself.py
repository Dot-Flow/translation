def translateJaMo(text: str):
    # 대응할 수 있는 한글 특수문자들 전처리
    for key, value in kor_punc_dict.items():
        text = text.replace(key, value)
        
    result = ""
    with_word_flag = False
    for ch in text:
        if "가" <= ch <= "힣":
            with_word_flag = True
            break
    
    for i, ch in enumerate(text):
        if ch in kor_ja_with_word:
            if with_word_flag:
                result += kor_ja_with_word[ch]
            else:
                result += kor_ja_only[ch]
        else:
            result += ch
            
    # print("result: ", result)
    return result  